    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        try{
            actividadTarea tarea = new actividadTarea();
            
            SimpleDateFormat fechaFormato = new SimpleDateFormat("dd/MM/yyy");
            String fecha = fechaFormato.format(this.chsDia.getDate());
            
            tarea.setNombre(this.txtNombre.getText().trim());
            tarea.setDescripcion(this.txtDescripcion.getText().trim());
            tarea.setFecha(fecha);
            tarea.setTerminada(false);
            
            if (new RegistroActividad().agregarTarea(tarea) == true){
                javax.swing.JOptionPane.showMessageDialog(this, "Tarea Agregada");
            }else {
                javax.swing.JOptionPane.showMessageDialog(this, "No se pudo agregar la tarea");
            }
            
            //ver como pasar el dato a sql
            
            
        }catch (Exception e){
            javax.swing.JOptionPane.showMessageDialog(this, "Error en "+e.getMessage());
        }
        
        
        
    }//GEN-LAST:event_btnAgregarActionPerformed

