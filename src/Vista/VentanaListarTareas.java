    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        try{
            boolean terminada = false;
            actividadTarea tarea = new actividadTarea();
            DefaultTableModel tbModelo = (DefaultTableModel) tbTareas.getModel();
            SimpleDateFormat fechaFormato = new SimpleDateFormat("dd/MM/yyy");
            String fecha = fechaFormato.format(this.chsDate.getDate());
            String tbId = tbModelo.getValueAt(tbTareas.getSelectedRow(), 0).toString();
            int Id = Integer.parseInt(tbId);
            
            if(this.rbSi.isSelected()){
                terminada = true;
            } else {
                terminada = false;
            }
            
            
            tarea.setNombre(this.txtNombre.getText().trim());
            tarea.setFecha(fecha);
            tarea.setDescripcion(this.txtDescripcion.getText());
            tarea.setTerminada(terminada);
            
            if(new RegistroActividad().actualizarTarea(tarea,Id)){
                javax.swing.JOptionPane.showMessageDialog(this, "Actualizado");
                DefaultTableModel modelo;
                modelo = RegistroActividad.listarTareas("");
                
                this.tbTareas.setModel(modelo);
                
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "No se pudo actualizar");
            }
        
        
        }catch(Exception e ){
            javax.swing.JOptionPane.showMessageDialog(this, "Error listar tareas en " + e.getMessage());
        }

    }//GEN-LAST:event_btnGuardarActionPerformed

