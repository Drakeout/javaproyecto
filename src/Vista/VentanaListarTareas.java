    private void tbTareasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbTareasMouseClicked
        DefaultTableModel tbModelo = (DefaultTableModel) tbTareas.getModel();

        String tbId = tbModelo.getValueAt(tbTareas.getSelectedRow(), 0).toString();
        String tbNombre = tbModelo.getValueAt(tbTareas.getSelectedRow(), 1).toString();
        String tbDesc = tbModelo.getValueAt(tbTareas.getSelectedRow(), 2).toString();
        String tbFecha = tbModelo.getValueAt(tbTareas.getSelectedRow(), 3).toString();
        String tbTerminada = tbModelo.getValueAt(tbTareas.getSelectedRow(), 4).toString();

        Date date1 = null;
        try {
            date1 = new SimpleDateFormat("dd/MM/yyyy").parse(tbFecha);
        } catch (ParseException ex) {
            System.out.println("Error en " + ex.getMessage());
        }

        txtId.setText(tbId);
        txtNombre.setText(tbNombre);
        txtDescripcion.setText(tbDesc);
        chsDate.setDate(date1);
        if (tbTerminada.equals("true")) {
            rbSi.setSelected(true);
        } else {
            rdNo.setSelected(true);
        }
    }//GEN-LAST:event_tbTareasMouseClicked

